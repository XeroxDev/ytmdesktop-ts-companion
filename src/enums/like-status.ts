/**
 * The like status.
 */
export enum LikeStatus {
    /**
     * Unknown like status.
     */
    UNKNOWN = -1,
    /**
     * Dislike status.
     */
    DISLIKE = 0,
    /**
     * Indifferent status. (neither like nor dislike)
     */
    INDIFFERENT = 1,
    /**
     * Like status.
     */
    LIKE = 2,
}